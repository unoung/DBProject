    ,IN_CHECKOUT_TIME	IN 	VARCHAR2		-- 체크아웃 시간
	,O_CUR			OUT		SYS_REFCURSOR
  );
  
  -- 숙소 수정
  PROCEDURE PROC_UP_LOD(
	IN_L_ID			IN		VARCHAR2
	,IN_G_COM_ID	IN		VARCHAR2
	,IN_L_COM_ID	IN		VARCHAR2
	,IN_L_NAME		IN		VARCHAR2
	,IN_CHECK_IN	IN		VARCHAR2
	,IN_CHECK_OUT	IN		VARCHAR2
	,O_ERRCODE		OUT		VARCHAR2
	,O_ERRMSG		OUT		VARCHAR2
  );
  
  -- 숙소 삭제
  PROCEDURE PROC_DEL_LOD(
	IN_L_ID			IN		VARCHAR2
	,O_ERRCODE		OUT		VARCHAR2
	,O_ERRMSG		OUT		VARCHAR2
  );
  
END PKG_LODGING;